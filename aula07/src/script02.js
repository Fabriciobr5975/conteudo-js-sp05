// Exemplo inicial laço de repetição

for (let contadora = 0; contadora < 10; contadora++) {
    console.log(contadora);
}

console.log("");

// Exemplo inicial laço de repetição while
let contadora = 0;

while (contadora < 10) {
    console.log(contadora);
    contadora++
}

console.log("");

// Exemplo inicial laço de repetição do while
let contador2 = 0;

do {
    let texto = "teste";
    console.log(texto)
    contador2++;

} while (contador2 < 10);